#DEFINIMOS UNA TUPLA
cocina = ('cuchara', 'cuchillo', 'tenedor')
print(cocina)

#saber el largo de una tupla con len
print(len(cocina))

#Acceder a un elemento se usan [] no ()
print(cocina[0])
print(cocina[-2])

#Acceder a un rango
print(cocina[0:2])
#Las tuplas necesitan si o si una coma (,) para que sea tupla y no str